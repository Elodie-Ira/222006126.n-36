//The program that asks the user to enter days using switchcase
public class switchcase {//The class name
    public static void main(String[] args){
        int day = 4;//Initialisation
        switch(day){
            case 1:
              System.out.println("monday");//The output
              break;
            case 2:
              System.out.println("tuesday");//The output
              break;
            case 3:
              System.out.println("wednesday");//The output
              break;
            default:
              System.out.println("unknown");//The output
              break;      

        }
    }
}
